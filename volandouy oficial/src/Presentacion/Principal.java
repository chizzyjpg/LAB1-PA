    private void cmdRegistroUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdRegistroUsuarioActionPerformed
        // TODO add your handling code here:
        
        //Obtengo datos de los controles Swing
        String nombreU = this.txtnombreUsu.getText();
        String apellidoU = this.txtapellido.getText();
        String ciU = this.txtCI.getText();
        
        //A través de mi interfaz registro a un nuevo usuario en mi Sistema
        ICU.registrarUsuario(nombreU, apellidoU, ciU);
        
        //Limpio el internal Frame
        this.txtnombreUsu.setText("");
        this.txtapellido.setText("");
        this.txtCI.setText("");
        this.internalRU.setVisible(false);
        
        //Muestro éxito de la operación
        JOptionPane.showMessageDialog(this, "El Usuario se ha creado con éxito", "Registrar Usuario", JOptionPane.INFORMATION_MESSAGE);
       
    }//GEN-LAST:event_cmdRegistroUsuarioActionPerformed

