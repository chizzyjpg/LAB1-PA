    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (this.txtCIUsuario.getText().equals("")){
            this.txtCIUsuario.setText("Ingresar CI");
        }
        else{
                    
            DataUsuario du = ICU.verInfoUsuario(this.txtCIUsuario.getText());
            this.txtNombreUsuario.setText(du.getNombre());
            this.txtApellidoUsuario.setText(du.getApellido());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

